package co.grandcircus.csHallOfFameapi.model;

public class TinyInventor {

	private String name;
	private String invented;
	private int year;
	
	public TinyInventor() {}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getinvented() {
		return invented;
	}

	public void setinvented(String invented) {
		this.invented = invented;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}
	
	
}
